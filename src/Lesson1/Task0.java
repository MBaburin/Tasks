package Lesson1;

public class Task0 {
    public static void main(String[] arg){
        int month = 5;
        int day = 29;

        System.out.println(month+day);
        System.out.println(month-day);
        System.out.println(month/day);
        System.out.println(month*day);

        System.out.println(month+=day);
        System.out.println(month-=day);
        System.out.println(month/=day);
        System.out.println(month*=day);

        System.out.println(month++);
        System.out.println(month--);
        System.out.println(day++);
        System.out.println(day--);
    }
}
